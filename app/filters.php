/*
|--------------------------------------------------------------------------
| Authentication Filters
|--------------------------------------------------------------------------
|
| The following filters are used to verify that the user of the current
| session is logged into this application. The "basic" filter easily
| integrates HTTP Basic authentication for quick, simple checking.
|
*/

Route::filter('auth', function()
{
	if (Auth::guest())
	{
		if (Request::ajax())
		{
			return Response::make('Unauthorized', 401);
		}
		else
		{
			return Redirect::guest('login');
		}
	}
});

/*
|--------------------------------------------------------------------------
| Authentication Filters
|--------------------------------------------------------------------------
|
| The following filters are used to verify role of the user of the current
| session logged into this application. Available roles
| * ADMIN - Admin
| * RDM_RESEARCHER - rdm researcher 
| * END_USER - participant
|
*/
Route::filter('role', function()
{
    $user = Auth::user();
    switch($user->role)
    {
        case 'ADMIN':
            return Redirect::action('DashBoardController@showAdminPage');
            
        break;
        case 'RDM_RESEARCHER':
           return Redirect::action('DashBoardController@showAdminPage');
        break;
        case 'END_USER':
            return Redirect::action('DashBoardController@showParticipantsPage');
            
        break;
    
    }
});

Route::filter('auth.basic', function()
{
	return Auth::basic();
});

/*
|--------------------------------------------------------------------------
| Guest Filter
|--------------------------------------------------------------------------
|
| The "guest" filter is the counterpart of the authentication filters as
| it simply checks that the current user is not logged in. A redirect
| response will be issued if they are, which you may freely change.
|
*/

Route::filter('guest', function()
{
	if (Auth::check()) 
                return Redirect::route('dashboard_admin')
                        ->with('flash_notice', 'You are already logged in!');
});

/*
|--------------------------------------------------------------------------
| CSRF Protection Filter
|--------------------------------------------------------------------------
|
| The CSRF filter is responsible for protecting your application against
| cross-site request forgery attacks. If this special token in a user
| session does not match the one given in this request, we'll bail.
|
*/

Route::filter('csrf', function()
{
	if (Session::token() !== Input::get('_token'))
	{
		throw new Illuminate\Session\TokenMismatchException;
	}
});
